package sqlite;

public class mes<a, b, c> {

    a t1;
    b t2;
    c t3;
    public mes(a t1, b t2, c t3){
        this.t1 = t1;
        this.t2 = t2;
        this.t3 = t3;
    }
    public a getT1(){
        return t1;
    }
    public b getT2(){
        return t2;
    }
    public c getT3(){
        return t3;
    }
}
